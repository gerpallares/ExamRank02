#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac == 2)
	{
		i = 0;
		while (av[1][i] == ' ' || av[1][i] == '\t' || av[1][i] == '\n')
			i++;
		while (av[1][i])
		{
			if (av[1][i] != ' ' && av[1][i] != '\t' && av[1][i] != '\n')
				write(1, &av[1][i], 1);
			else if (av[1][i + 1] != ' ' && av[1][i + 1] != '\t' && av[1][i + 1] != '\n' && av[1][i + 1] != '\0')
				write(1, "   ", 3);
			i++;
		}
	}
	write(1, "\n", 1);
}
